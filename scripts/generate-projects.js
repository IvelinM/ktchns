#!/usr/bin/env node
/**
 * Scans assets/images/projects, finds every subfolder and its images,
 * then writes src/app/projects/projects.data.ts automatically.
 *
 * Run manually:  node scripts/generate-projects.js
 * Runs automatically before every build via the "prebuild" npm script.
 */

const fs   = require('fs');
const path = require('path');

const IMAGE_EXTS  = new Set(['.webp']);
const PROJECTS_DIR = path.join(__dirname, '..', 'assets', 'images', 'projects');
const OUT_FILE     = path.join(__dirname, '..', 'src', 'app', 'projects', 'projects.data.ts');

// ── Collect projects ──────────────────────────────────────────────────────────

const folders = fs.readdirSync(PROJECTS_DIR, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .map(d => d.name)
  .sort();

const projects = folders.map(folder => {
  const dir    = path.join(PROJECTS_DIR, folder);
  const images = fs.readdirSync(dir)
    .filter(f => IMAGE_EXTS.has(path.extname(f).toLowerCase()))
    .sort()
    .map(f => `assets/images/projects/${folder}/${f}`);

  return { id: folder.toLowerCase(), name: folder, cover: images[0] ?? '', images };
}).filter(p => p.images.length > 0);

// ── Pick hero images: first image from each project, cycle until 6 ───────────

const heroImages = [];
const maxHero = 6;
let i = 0;
while (heroImages.length < maxHero && projects.length > 0) {
  const project = projects[i % projects.length];
  const img = project.images[Math.floor(i / projects.length)];
  if (img) heroImages.push(img);
  i++;
}

// ── Generate TypeScript source ────────────────────────────────────────────────

function formatProject(p) {
  const imgs = p.images.map(img => `    '${img}',`).join('\n');
  return `  {
    id: '${p.id}',
    name: '${p.name}',
    cover: '${p.cover}',
    images: [\n${imgs}\n    ],
  }`;
}

const src = `// AUTO-GENERATED by scripts/generate-projects.js — do not edit by hand.
// Add or rename folders inside assets/images/projects/, then run: npm run generate
// (runs automatically before every build)

export interface Project {
  id: string;
  name: string;
  cover: string;
  images: string[];
}

export const PROJECTS: Project[] = [
${projects.map(formatProject).join(',\n')}
];

export const HERO_IMAGES: string[] = [
${heroImages.map(img => `  '${img}',`).join('\n')}
];
`;

fs.writeFileSync(OUT_FILE, src, 'utf8');
console.log(`[generate-projects] wrote ${projects.length} project(s) → ${OUT_FILE}`);
